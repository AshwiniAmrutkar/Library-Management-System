    private void student_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_student_searchActionPerformed
        try{
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/Library_Management_System", "root", "Ashwini987");
            String sql = "select * from student where student_id = ?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, student_id.getText());
            ResultSet rs = st.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(null, "no matches");
            }
            else{
                name1.setText(rs.getString("name"));
                fathers_name.setText(rs.getString("father"));
                course.setText(rs.getString("course"));
                branch.setText(rs.getString("branch"));
                year.setText(rs.getString("year"));
                semester.setText(rs.getString("semester"));
            }

            st.close();
            rs.close();
        }
        catch(Exception e){

        }
    }//GEN-LAST:event_student_searchActionPerformed

