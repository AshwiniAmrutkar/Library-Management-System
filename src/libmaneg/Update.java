    private void UPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UPDATEActionPerformed
        try{
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/Library_Management_System", "root", "Ashwini987");

            String sql = "update student set name=?,father=?,course=?,branch=?,year=?,semester=? where student_id=?";
            PreparedStatement st = con.prepareStatement(sql);
            
            st.setString(1, name1.getText());
            st.setString(2, fathers_name.getText());
            st.setString(3, course.getText());
            st.setString(4, branch.getText());
            st.setString(5, year.getText());
            st.setString(6, semester.getText());
            st.setString(7, student_id.getText());

            int i=st.executeUpdate();
            if (i > 0 ){
                JOptionPane.showMessageDialog(null,"Student with id = " +student_id.getText()+" has been successfully updated to "+name1.getText()+ " and " +fathers_name.getText());

            }

        }
        catch(Exception e){

            JOptionPane.showMessageDialog(null, "error");
        }

        

        this.setVisible(false);
        new Home11().setVisible(true);
    }//GEN-LAST:event_UPDATEActionPerformed

