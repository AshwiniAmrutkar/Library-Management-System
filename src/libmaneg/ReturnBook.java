    private void returnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnBookActionPerformed
        try{
            Connection c =DriverManager.getConnection("jdbc:mysql://localhost:3306/Library_Management_System", "root", "Ashwini987");
            String sql = "insert into returnBook(book_id, student_id, bname, sname,course, branch, dateOfIssue, dateOfReturn) values(?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement st = c.prepareStatement(sql);
            st.setString(1, book_id.getText());
            st.setString(2, student_id.getText());
            st.setString(3, book_name.getText());
            st.setString(4, student_name.getText());
            st.setString(5, course.getText());
            st.setString(6, branch.getText());
            st.setString(7, date_of_issue.getText());
           //st.setString(8, date_of_return.getText());
           st.setString(8, ((JTextField) dateOfReturn.getDateEditor().getUiComponent()).getText());

            int i = st.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Processing..");
                delete();
            } else
            JOptionPane.showMessageDialog(null, "error");

        }
        catch(Exception e){
        }
    }//GEN-LAST:event_returnBookActionPerformed

