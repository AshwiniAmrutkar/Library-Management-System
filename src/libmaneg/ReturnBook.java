    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{
            Connection c =DriverManager.getConnection("jdbc:mysql://localhost:3306/Library_Management_System", "root", "Ashwini987");
            String sql = "select * from issueBook where book_id = ? and student_id =?";
            PreparedStatement st = c.prepareStatement(sql);
            st.setString(1, book_id.getText());
            st.setString(2, student_id.getText());
            ResultSet rs = st.executeQuery();
            
            if(!rs.next()){
                JOptionPane.showMessageDialog(null, "no matches");
            }
            else{

            //while (rs.next()) {
                book_name.setText(rs.getString("bname"));
                student_name.setText(rs.getString("sname"));
                course.setText(rs.getString("course"));
                branch.setText(rs.getString("branch"));
                date_of_issue.setText(rs.getString("dateOfIssue"));
            }
            st.close();
            rs.close();
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(this,"No book is issued on this id currently");
        }
    }//GEN-LAST:event_searchActionPerformed

